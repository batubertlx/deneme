package com.YoklamaSistemi.Entity;

/**
 *
 * @author ogrenci
 */
public class Siniflar 
{
    private int sinifId ;
    private String sinifKodu ;
    private int sinifProgrami ;

    /**
     * @return the sinifId
     */
    public int getSinifId() {
        return sinifId;
    }

    /**
     * @param sinifId the sinifId to set
     */
    public void setSinifId(int sinifId) {
        this.sinifId = sinifId;
    }

    /**
     * @return the sinifKodu
     */
    public String getSinifKodu() {
        return sinifKodu;
    }

    /**
     * @param sinifKodu the sinifKodu to set
     */
    public void setSinifKodu(String sinifKodu) {
        this.sinifKodu = sinifKodu;
    }

    /**
     * @return the sinifProgrami
     */
    public int getSinifProgrami() {
        return sinifProgrami;
    }

    /**
     * @param sinifProgrami the sinifProgrami to set
     */
    public void setSinifProgrami(int sinifProgrami) {
        this.sinifProgrami = sinifProgrami;
    }
    
}
